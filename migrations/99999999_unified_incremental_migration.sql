    index_count INTEGER;
BEGIN
    SELECT COUNT(*) INTO index_count FROM pg_indexes
    WHERE schemaname = 'public' AND indexname LIKE 'idx_%';

    RAISE NOTICE '==========================================';
    RAISE NOTICE '历史综合增量兼容脚本执行完成';
